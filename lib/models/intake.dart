class Intake {
  final int id;
  final String name;
  final double grams;
  final double calories;
  final double protein;
  final double carbs;
  final double fat;
  final DateTime date;

  Intake({
    required this.id,
    required this.name,
    required this.grams,
    required this.calories,
    required this.protein,
    required this.carbs,
    required this.fat,
    required this.date,
  });
}
